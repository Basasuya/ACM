const int MAXN =  1e5 + 5;
char S[MAXN];

int main() {
    int T;
    scanf("%d", &T);
    while(T --) {
        int p;
        int price[2];
        scanf("%d %d %d", &price[0], &price[1], &p);
        scanf("%s", S);

        int len = strlen(S);
        int cost = 0;

        int ans = len - 1;
        for(int i = len - 2; i >= 0; --i) {
            if(i == len - 2 || S[i] != S[i + 1]) {
                cost += price[S[i] - 'A'];
            }

            // debug(cost, i);

            if(cost <= p) {
                ans = min(ans, i);
            } else break;
        }

        printf("%d\n", ans + 1);
    }
    return 0;
}